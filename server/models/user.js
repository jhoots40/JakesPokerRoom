const mongoose = require("mongoose");

const userSchema = new mongoose.Schema({
  username: {
    type: String,
    required: true,
    unique: true,
    trim: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
    trim: true,
    lowercase: true,
  },
  password: {
    type: String,
    required: true,
    trim: true,
  },
  // You can add more fields based on your application's requirements
  // For example: firstName, lastName, dateOfBirth, etc.
});

module.exports = mongoose.model("Users", userSchema);
